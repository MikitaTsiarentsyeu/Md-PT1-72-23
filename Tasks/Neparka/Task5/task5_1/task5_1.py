"""Модуль содержит в себе функцию, предназначенную для сложения двух 
целых чисел."""


def sum_of_two_ints(int1: int, int2: int) -> int:
    """Принимает в качестве аргументов два целых числа и возвращает их 
    сумму.

    Ввод аргументов, отличных от типа "int", - может привести к 
    неккоректной работе функции."""

    return int1 + int2
